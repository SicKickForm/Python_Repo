import math

# Math module methods

# -1 to 1
print(math.acos(-1))
print(math.asin(1))
print(math.cos(0))
print(math.sin(0))

# >= 1
print(math.acosh(1))

# any
print(math.e)
print(math.inf)
print(math.nan)
print(math.pi)
print(math.tau)
print(math.asinh(0))
print(math.atan(0))
print(math.atan2(1, 1))
print(math.cosh(30))
print(math.erf(5))
print(math.erfc(5))
print(math.exp(5))
print(math.expm1(5))
print(math.fmod(10, 3))
print(math.frexp(25))
print(math.gcd(12, 30))
print(math.sqrt(25))
print(math.ceil(4.4))
print(math.comb(10, 3))
print(math.copysign(-5, 2))
print(math.fabs(-5))
print(math.factorial(5))
print(math.floor(5.6))
print(math.isclose(1.25, 1.251))
print(math.isfinite(0))
print(math.isinf(1))
print(math.isnan(1))
print(math.ldexp(5, 2))
print(math.perm(10, 3))
print(math.pow(5, 2))
print(math.remainder(5, 2))
print(math.sinh(30))
print(math.tan(30))
print(math.tanh(30))
print(math.trunc(5.2))

# -0.99 to 0.99
print(math.atanh(0.5))

# radian
print(math.degrees(math.pi))
print(math.radians(math.pi))

# coordinated points
print(math.dist((3, 4), (0, 0)))
print(math.hypot(10, 5))

# array
A = (1, 2, 3)
print(math.fsum((1, 2, 2)))
print(math.prod(A))

# >= 0
print(math.gamma(25))
print(math.lgamma(25))
print(math.isqrt(10))
print(math.log(1))
print(math.log10(1))
print(math.log1p(0))
print(math.log2(1))
